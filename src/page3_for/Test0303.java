package page3_for;

public class Test0303 {
	/*
	 * do-while循环（直到型循环）
	 * 
	 * [initialization]			初始化条件
	 * do{
	 * 		body；				循环体
	 * 		[iteration;] 		迭代，变更循环条件
	 * }while(termination);		循环条件
	 * 
	 * 首先执行一遍{}中的语句，当表达式termination为true时，继续执行{}中的语句，否则终止循环
	 */
	
	public static void main(String[] args) {
		
		int i = 0;
		do {
			System.out.println("0 0 0 0" +"--" + i);
			i++;
			
		}while(i < 5);
		
		

	}
	
	
	
	
	
	
	
	
}
